<p>The objectives of the association are as follows-</p>
<ul>
<li>Re-establish full-fledged contact with the past students of NIT, Jamshedpur</li>
<li>To conduct several programmes under the banner of the association for the benefit of the students</li>
<li>To plan and implement several small projects to commemorate NIT JSR Alumni Association’s presence in NIT Jamshedpur</li>
<li>Improve Alumni Relations with institute on various fronts</li>
<li>Enabling and sustaining a parallel source of funding for institute by tapping alumni resources</li>
<li>Creating and Enhancing brand equity of NIT Jamshedpur and contribute to alumni brand building </li>
<li>Increase meaningful interaction between student-alumni and faculty-alumni on a sustained basis for mutual benefit</li>
</ul>
