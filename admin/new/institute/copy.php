  <span>About Us</span>
</div>
<div class="pagecontent">
<p>National Institute of Technology, Jamshedpur was originally founded as Regional Institute of Technology in 1960 by Dr. Srikrishna Sinha, the then chief minister of Bihar with the aim of nurturing talent and setting high standards of education and excellence. Having been a freedom fighter himself, he chose the auspicious day of 15th August, 1960 to lay the foundation stone. Later upgraded to National Institute of Technology on 27th December, 2002, with the status of a Deemed University, it is now totally under the control of the Ministry of Human Resources Development, Government of India, New Delhi since 1st April, 2003.
</p>
<p>The Institute has several departments in various disciplines of engineering and the pure sciences, with nearly 100 laboratories organized on a unique pattern of functioning.</p>
<p>Situated in the heart of the rich mineral and industrial belt of Jharkhand State, the campus encompasses the Main building, the Library, the Computer Center, 11 hostels (9 Boys’ hostels and 2 Girls’ hostels), staff quarters, Guest House, a Students’ Gymkhana, a Post-office , a branch of State Bank of India, an ATM, central store and a dispensary. The hostels and departments are connected with high speed wireless LAN. The institute maintains courts for basketball, volleyball, badminton and tennis. Apart from this, it also has cricket, hockey and football grounds.</p>
<p>The institute is poised for bright and promising future. The annual system of the curricula has been changed to semester system. The courses have been completely restructured and prepared as per the needs of society and industries.</p>
<p>There is refreshing development on every front. The institute has achieved impressive placement for its students, with top industrial houses and software companies recruiting the students in large numbers.</p>
<span class="pagesubheading">Location</span>
<img src="../images/h2.png" width="100%" />
<p>NIT Jamshedpur is situated at a distance of 12 kilometers from Tatanagar Railway Station, on the banks of the rivers Kharkai. It is just far enough to ensure that the city with its noise is too distant to distract and just near enough to guarantee that all city facilities are immediately available. The institute is strategically located with the unique advantage of being surrounded by giant and medium scale industries like Tata-Steel, Telco, Indian Steel Wire Products, Tata Tubes, Tinplate Company, Tata Timken etc. and reputed Institutes like the National Metallurgical Laboratory, Xavier Labour Relations Institute and National Institute of Foundry and Forge Technology, Ranchi.</p>
