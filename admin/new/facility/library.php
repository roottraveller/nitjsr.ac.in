    <span class="pagesubheading">Book Bank</span>
    <img src="<?php echo $root?>images/h2.png" width="100%" />
<p>There are two Book Banks in the Library.</p>
<ol type="1">
	<li>One of them is meant for the students belonging to scheduled cast (SC) & Scheduled Tribe (ST). It was established under a scheme sanctioned by the government of Bihar (Welfare Department). Under this scheme the Welfare department, earlier Bihar Govt. but now Jharkhand Government, through their Disctrict Welfare Officer, supplies books & Almirahs. As many as 15 books are issued for one academic session to a SC /ST student from such a Book Bank.
    </li>
	<li>
    The other Book Bank is TEQIP Book Bank, used by both SC/ST students and General students. TEQIP Book Bank is governed & funded by World Bank Aid. Two Books are issued from this Book Bank for one academic session to a student.
    </li>
</ol>
