<p align="center"><b><font color="green">ABOUT THE PROGRAMME</font></b></p><br/>
<p>Numerical Analysis is a promising discipline in shaping future research and development activities in both academic and industry. It naturally finds applications in all fields of engineering and the physical sciences. The subject of Numerical Analysis provides Numerical Methods for the study and solution of mathematical problems arising in the field of Science and Engineering. Due to the enormous progress in hi-speed computer technology in recent years, the use of numerical methods has become more and more attractive and important. Numerical methods in many cases offer cost effective and, therefore, very attractive possibility for the investigation and optimization of products and processes. The successful and efficient application of such methods requires certain basic knowledge about the underlying methodologies and their possibilities with respect to specific applications. The main objective of this programme is to provide preliminaries of the Numerical Analysis and to make aware of the various numerical techniques to solve mathematical problems that cannot be solved by exact methods and deliver some state of the art lectures on the advanced topics by experts. </p>
<p>&nbsp;</p>
<p><b>The topics to be covered include:</b>
<br/><ul><li>Equations and System of Linear and Nonlinear equations</li>
<li>Interpolation and Approximations</li>
<li>Differentiation and Integrations</li>
<li>Ordinary and Partial differential equations</li>
<li>Theory of Computation and Parallel Computing</li>
<li>Computational fluid dynamics</li>
<li>Singular perturbations and</li>
<li>Fractional differential equations.</li></ul>
<br/>Use of MatLab software is planned to give hands on
experience in the practical/laboratory sessions.</p>
