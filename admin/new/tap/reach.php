<div class="pagecontent">
<p>Jamshedpur is the first well-planned industrial city of India, founded by the late Jamshedji Nusserwanji Tata and ranks 28th among the 35 million-plus cities and is also the 31st urban agglomeration in India according to the census 2001. Located in the East Singhbhum district of Jharkhand on the Chota Nagpur plateau, it is the district headquarters and is surrounded by the beautiful Dalma Hills. The rivers Subarnarekha and Kharkai border the North and West of the city, respectively. NIT Jamshedpur, since its establishment in 1960, was in Singhbhum district. After division of the district in 1990, its location came under the West (Pashchimi) Singhbhum district. After further division of the West Singhbhum district in 2001, NIT Jamshedpur finally came under Saraikela-Kharswan district of Jharkhand. The institute is located in Adityapur town of the district which is at the border of Saraikela-Kharswan district and East Singhbhum district. Adityapur is a highly industrialized town having hundreds of small and medium scale industries.</p>
<span class="pagesubheading">Airways</span>
<img src="<?php echo $root?>images/h2.png" width="100%" />
<p><b>Birsa Munda Airport, Ranchi, Jharkhand</b></p>
<p>Ranchi is connected with Jamshedpur through NH-33 via Chandil and Bundu. Road distance is about 140 KM between the two cities.  Please add about 15 KM more to this distance as the Airport is about 5 KM from Ranchi city and NIT Jamshedpur is about 10 KM from the Jamshedpur bus-stand. Regular bus services are available from Ranchi  to Jamshedpur.</p>
<p>
Ranchi is also connected with Jamshedpur by rail-route. Distance is 167 KM by train.  Two express trains Ranchi-Howrah Intercity Express (three days a week THU, FRI, SAT) and Hatia-Howrah Express (daily) as well as two local trains are available between Ranchi railway station and Tatanagar Jn.
</p>





<span class="pagesubheading">Railways</span>
<img src="<?php echo $root?>images/h2.png" width="100%" />
<p>The railway station of <b>Jamshedpur</b> is known as <b>Tatanagar Junction</b>.Tatanagar (Jamshedpur) is an important railway junction &amp; a model station on the South Eastern Railway and is the most important railway junction of the state as it is connected directly to all the major cities of India, viz., Kolkata, Mumbai, Delhi, Chennai, Bangalore, Ahmedabad, Amritsar, Raipur, Patna, Nagpur, Kanpur, Ranchi, Pune, Jammu, Vishakapatnam, Guwahati, Bhubaneshwar etc.Tatanagar Jn is 135 KM from Kharagpur and 250 KM from Howrah (Kolkata) by train route. It is well connected to Kharagpur and Howrah Junctions through over a dozen of super-fast/express/local trains.</p>




<span class="pagesubheading">Roadways</span>
<img src="<?php echo $root?>images/h2.png" width="100%" />
<p>Jamshedpur is well connected to other parts of India through national and state highways. The major highways are:</p>
<ul><li>National Highway 33 (NH 33) touches the city and joins the NH32, which connects Kolkata with Delhi.</li>
<li>National Highway 32 (NH 32) connects Jamshedpur to Govindpur via Dhanbad.</li></ul>
<p>Regular bus services are available from Jamshedpur to Ranchi (131 km), Patna, Gaya, Kolkata (300 km) via Baharagora, Hazaribagh, Puri, Bhubaneshwar, Asansol, Bokaro, Dhanbad etc. However, Roadways travel on Kolkata-Kharagpur-Jamshedpur route is not recommended as train route is much better (faster, cheaper, safe and comfortable).</p>


<span class="pagesubheading">Direction to reach the Institute from Tatanagar railway station and Jamshedpur main Bus Stand(Sakchi):</span>
<img src="<?php echo $root?>images/h2.png" width="100%" />
<p>The Institute’s Administrative Building is 9.5 KM from Jamshedpur main bus stand at Sakchi, 8.5 KM from Tatanagar railway station and only 2 KM from Adityapur railway station which is a small station.  Distance between Adityapur railway station and Tatanagar Jn is 4 KM. While coming from Howrah side, Tatanagar Jn comes before Adityapur station, and while coming from Mumbai/ Bilaspur/ Durg/ Rourkela/ Patna/ Asansol side, Adityapur station comes before Tatanagar Jn. All local trains and few express trains stop at Adityapur station. Therefore, while coming from Mumbai/Nagpur/Bilaspur/Durg/Rourkela/Patna/Asansol side, one should get down if the train stops at Adityapur station and hire a reserved auto-rickshaw or taxi (fare Rs 40/- as in Aug 2012) for the institute. </p>
<p>Frequent local transports are available from Jamshedpur main Bus stand, Sakchi and Tatanagar Jn on reserved as well as shared basis. However, in case of a shared transport (such as an auto-rickshaw or a city bus), one may need to change at Sakchi, Bistupur and Adtyapur-1.</p>
<p>From Tatanagar railway station or Sakchi main bus stand, one may travel by taxi/auto-rickshaw via Bistupur and Adityapur-1. At Adityapur-Kandra main road, taxi should turn left at All India Radio Building towards the railway under-bridge to travel along the NIT road to reach the Institute which is located at Adityapur-2. The Institute Administrative Building is 3.5 KM from the All India Radio located at Adityapur-1. The fare charges by taxi/auto cost between Rs 120/- (approx.) on reserved basis and Rs 15/- to 18/- on shared basis from Tatanagar Railway Station to NIT Jamshedpur Administrative Building. [Fare given as in Aug 2012]. </p>
<p>There is an alternative route (<i>about 0.5 KM longer than the above mentioned route via railway under-bridge and NIT road</i>) to the institute campus via RIT MORE (also pronounced as “mod”) from Adityapur-Kandra main road. While coming from Bistupur side, in stead of turning left near All India Radio Building at Adityapur-1, one may go ahead straight and take left turn at RIT MORE which is about 2 KM from All India Radio Building. The institute administrative building is about 2 KM from RIT MORE.</p>


